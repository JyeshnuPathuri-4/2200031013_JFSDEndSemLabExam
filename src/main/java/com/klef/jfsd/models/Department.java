package com.klef.jfsd.models;

public class Department {
    private int sid;
    private String sname;
    private double slocation;
    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }

    public double getSlocation() {
        return slocation;
    }

    public void setSlocation(double sfee) {
        this.slocation = sfee;
    }
}